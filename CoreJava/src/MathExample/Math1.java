package MathExample;

public class Math1 {
	public static void main(String[] args) {
		int a=16;
		int b=5;
		
		System.out.println(Math.max(a, b));
		System.out.println(Math.sqrt(a));
		System.out.println(Math.pow(a, b));
		System.out.println(Math.log(a));
		System.out.println(Math.log(b));
		
	}

}
